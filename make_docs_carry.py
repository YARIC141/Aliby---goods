def new_doc():
    doc = Document()
    for s in doc.styles:
        if s.name == 'Normal':
            s.font.name = 'Times New Roman'
            s.font.size = Pt(12)
    for sec in doc.sections:
        sec.top_margin    = Cm(2.5)
        sec.bottom_margin = Cm(2.5)
        sec.left_margin   = Cm(3)
        sec.right_margin  = Cm(1.5)
    return doc


def h1(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text.upper())
    run.bold = True
    run.font.size = Pt(14)
    return p


def h2(doc, text):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(12)
    p.space_before = Pt(10)
    p.space_after  = Pt(4)
    return p


def para(doc, text, bold_prefix=None):
    p = doc.add_paragraph()
    p.space_after = Pt(2)
    if bold_prefix:
        run = p.add_run(bold_prefix)
        run.bold = True
        p.add_run(text)
    else:
        p.add_run(text)
    return p


def divider(doc):
    doc.add_paragraph()


